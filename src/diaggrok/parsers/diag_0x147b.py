    #   * ts-anchor — pairing each record's DIAG ts64 (log_time) with its body
    #     absolute GPS time gives a clean linear map ts64 -> GPS wall-clock:
    #     R²=0.99999985 (SDX55, n=662) / 0.99999939 (SDX20, n=56), slope a
