# $ + talker(2) + type(3) , body ... *XX. The body class excludes $, \r, \n so
# a match cannot run past a sentence boundary into the next sentence or into
# binary. Gated by NMEA 0183 XOR checksum so binary-float noise that happens to
# match the shape is rejected (the corpus walk found ZERO checksum-invalid
# matches across 25,052 sentences — the shape+checksum pair is precise).
_NMEA_RE = re.compile(rb"\$[A-Z]{2}[A-Z]{3},[\x20-\x7e]*?\*[0-9A-Fa-f]{2}")


def _extract_nmea(data: bytes) -> list[str]:
    """Checksum-valid NMEA sentences embedded in the LocEng snapshot body.

    0x1C90 is a heterogeneous LocEng state dump; the GNSS body region carries
    whole, checksum-valid NMEA across every constellation (GP/GL/GA/GB/GQ/GN).
    Presence is fractional and NOT keyed on ``seq_counter`` (a seq=0 record can
    carry 100+ sentences) — consistent with seq_counter being a counter, not a
    format key (#N v4). Returned in body order.
    """
    out: list[str] = []
    for m in _NMEA_RE.finditer(data):
        s = m.group().decode("ascii", "replace")
        if _verify_nmea_checksum(s):
            out.append(s)
    return out


