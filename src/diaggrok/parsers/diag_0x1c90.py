def parse_0x1c90(log_time: int, data: bytes) -> Diag0x1C90 | None:
    if len(data) < 2:
        return None
    version = data[0]
    if version != EXPECTED_VERSION_1C90:
        return None
    seq_counter = data[1]
    if len(data) >= 8:
        config_word = unpack_from('<I', data, 4)[0]
    else:
        config_word = 0
    if len(data) > 2:
        nonzero = sum(1 for b in data[2:] if b != 0)
        total = max(len(data) - 2, 1)
        density = round(nonzero / total, 2)
    else:
        density = 0.0
    nmea = _extract_nmea(data)
    talkers = tuple(sorted({s[:6] for s in nmea}))
    return Diag0x1C90(
        log_time=log_time,
        version=version,
        seq_counter=seq_counter,
        config_word=config_word,
        data_density=density,
        payload_size=len(data),
        body_raw=data[1:],
        nmea_sentences=tuple(nmea),
        nmea_talkers=talkers,
    )
