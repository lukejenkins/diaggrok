def parse_0x14a6(log_time: int, data: bytes) -> Diag0x14A6 | None:
    if len(data) != 151:
        return None
    # v3 (#N audit follow-up): hard gate on version byte before the
    # SV-slot loop. Audit flagged this as 'SV-slot loop reads bytes 23+
    # first' — i.e. byte 0 was never validated. Corpus: 3195 records / 9
    # chipsets all have version == 0x02.
    if data[0] != 0x02:
        return None
    slots: list[GnssSvCno14A6Slot] = []
    for i in range(12):
        base = 23 + i * 4
        slots.append(GnssSvCno14A6Slot(
            sv_id=unpack_from('<H', data, base)[0],
            cno_metric=unpack_from('<H', data, base + 2)[0],
        ))
    return Diag0x14A6(
        log_time=log_time,
        version=data[0],
        flag_1=data[1],
        stats_header=bytes(data[2:10]),
        reserved_10=data[10],
        nav_state=unpack_from('<H', data, 11)[0],
        reserved_13=data[13],
        num_sv_family_a=data[14],
        flag_15=data[15],
        num_sv_fix=data[16],
        num_sv_subset=data[17],
        num_sv_tracked=data[18],
        aux_19=data[19],
        aux_20=data[20],
        fw_tag=data[21],
        sub_type=data[22],
        sv_slots=slots,
        reserved_tail=bytes(data[71:151]),
        payload_size=len(data),
    )
