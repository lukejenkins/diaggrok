# 0x14A6 — GNSS per-SV CNo snapshot (151B fixed) (#N)
# ---------------------------------------------------------------------------
# Corpus: 3195 records / 151B fixed / 12 chipset+firmware pairs (2026-04-21).
#
# Full-byte decode (every byte named — zero body_raw):
#   [0]      u8  version = 2 (constant 3195/3195)
#   [1]      u8  flag_1 ∈ {0, 1}
#   [2:10]   8B  stats_header — 8 per-family status bytes
#   [10]     u8  reserved_10 = 0 (constant across RE corpus + EM7565).
#   [11:13]  u16 nav_state (LE) — RE'd in #N. 0x0000 (clear) or 0x0110
#                (set: byte[11]=0x10, byte[12]=0x01). Zero across the entire
#                3195-record cross-chipset RE corpus, but on Sierra EM7565
#                SWI9X50C it is a **latching GNSS receiver-state / nav-
#                maturity flag**: in the gnss_comparison_2026-06-06 capture
#                it transitions exactly ONCE (164 clear → 175 set, in
#                capture-time order) and never reverts. It is orthogonal to
#                the SV-count fields — two record shapes with byte-identical
#                counts (fam=fix=trk=6, subset=0, flag_15=0) differ only in
#                nav_state (12 clear vs 30 set), which rules out any "derived
#                from counts" reading. The clear→set edge coincides with the
#                receiver maturing from initial GPS-only acquisition (fix=5)
#                toward steady-state navigation (fix up to 7, tracked up to
#                8); C/N0 is comparable across both states so it is not a
#                signal-quality gate. Value is kept raw (size-invariance ≠
#                format-invariance) — a future capture may show other values.
#                Cross-modem scope (#N, <redacted-ref>): nav_state is
#                **EM7565-build-specific**. Across all 10 in-corpus EM7511
#                captures (1271 records, BOTH SWI9X50C_01.08.04.00 and
#                01.14.22.00 firmwares) offsets [11]/[12] are 0x00 in 1271/1271
#                — even though EM7511 is the same MDM9x50-class silicon and the
#                same SWI9X50C firmware family. So the flag is emitted only by
#                the EM7565 build's GNSS stack, not by the chipset family.
#   [13]     u8  reserved_13 = 0 (constant across RE corpus + EM7565).
#   [14]     u8  num_sv_family_a ∈ {0, 4, 5, 6, 8}
#   [15]     u8  flag_15 ∈ {0, 1}
#   [16]     u8  num_sv_fix ∈ {0..11} — sv-count-like field.  Matches the
#                populated-slot count on 2295/3195 records (72%); on the
#                remainder, populated is higher than byte[16] (typical
#                range: byte[16] < populated ≤ byte[18]).  Semantic
#                interpretation: count of SVs included in the current
#                fix, vs byte[18] which is the tracked-SV total.
#   [17]     u8  num_sv_subset ∈ {0..9}
#   [18]     u8  num_sv_tracked ∈ {0..12} — upper bound on populated
#                slot count; populated slots ≤ byte[18] on 3195/3195.
#   [19]     u8  aux_19
#   [20]     u8  aux_20
#   [21]     u8  fw_tag ∈ {0, 0x6d, 0x6e, 0x6f, 0x74, 0x75} — same build-channel
#                tag that 0x1509 exposes at its byte[1]: cross-parser
#                invariant. ASCII letters 'm'/'n'/'o'/'t'/'u' (sierra SWIX65C
#                contributed 't', #N; sierra EM7565 SWI9X50C contributed
#                'u', #N/<redacted-ref>).
#   [22]     u8  sub_type ∈ {0, 9} (= 0 only on empty records)
#   [23:71]  48B sv_slots — 12 slots × 4 bytes each:
#                - slot[i][0:2]  u16 LE sv_id
#                - slot[i][2:4]  u16 LE cno_metric
#                (sv_id and cno_metric values fit in u8 range, hence the
#                 "every other byte is zero" pattern.)
#                Populated count matches byte[16] on 3195/3195 records.
#   [71:151] 80B reserved_tail = 0 (constant 3195/3195)

@dataclass
class GnssSvCno14A6Slot:
    """One slot in the 12-slot SV table of 0x14A6."""
    sv_id: int         # u16 LE
    cno_metric: int    # u16 LE

    @property
    def is_populated(self) -> bool:
        return self.sv_id != 0 or self.cno_metric != 0


@dataclass
class Diag0x14A6:
    """GNSS per-SV CNo snapshot (0x14A6) — 151B fixed, fully decoded.

    Renamed from the pre-v2 stub (which called the same class GnssData14A6
    but only exposed version/sub_type/counter/body_raw).  The post-v2
    decode exposes every byte as a named field or structured slot.
    """
    log_time: int
    version: int
    flag_1: int
    stats_header: bytes      # [2:10]
    reserved_10: int         # [10]    constant 0
    nav_state: int           # [11:13] u16 LE — latching nav-maturity flag (#N)
    reserved_13: int         # [13]    constant 0
    num_sv_family_a: int     # [14]
    flag_15: int             # [15]
    num_sv_fix: int          # [16]  see module docstring for semantics
    num_sv_subset: int       # [17]
    num_sv_tracked: int      # [18]  upper bound on populated slots
    aux_19: int              # [19]
    aux_20: int              # [20]
    fw_tag: int              # [21]
    sub_type: int            # [22]
    sv_slots: list[GnssSvCno14A6Slot]  # 12 slots from [23:71]
    reserved_tail: bytes     # [71:151]
    payload_size: int

    @property
    def counter(self) -> int:
        """Back-compat alias (prior stub named byte[2] as `counter`)."""
        return self.stats_header[0]

    @property
    def reserved_mid(self) -> bytes:
        """Back-compat: the [10:14] blob, pre-#N a single `reserved_mid`
        field. Now reconstructed from reserved_10 / nav_state / reserved_13
        so existing consumers (and the zero-region tests on the RE-corpus
        fixtures) keep working."""
        return bytes((self.reserved_10,)) + \
            self.nav_state.to_bytes(2, 'little') + \
            bytes((self.reserved_13,))

    def to_dict(self) -> dict[str, Any]:
        return {
            'type': 'Diag0x14A6',
            'log_time': self.log_time,
            'version': self.version,
            'flag_1': self.flag_1,
            'stats_header': self.stats_header.hex(),
            # reserved_mid_zero now means the *truly*-reserved bytes [10] and
            # [13] are zero; [11:13] is the named nav_state field (#N).
            'reserved_mid_zero': self.reserved_10 == 0 and self.reserved_13 == 0,
            'nav_state': self.nav_state,
            'num_sv_family_a': self.num_sv_family_a,
            'flag_15': self.flag_15,
            'num_sv_fix': self.num_sv_fix,
            'num_sv_subset': self.num_sv_subset,
            'num_sv_tracked': self.num_sv_tracked,
            'aux_19': self.aux_19,
            'aux_20': self.aux_20,
            'fw_tag': self.fw_tag,
            'sub_type': self.sub_type,
            'sv_slots': [
                {'sv_id': s.sv_id, 'cno_metric': s.cno_metric}
                for s in self.sv_slots if s.is_populated
            ],
            'reserved_tail_zero': self.reserved_tail == b'\x00' * 80,
            'payload_size': self.payload_size,
        }


