@dataclass
class Gnss19EBL5Entry:
    """Single 70-byte per-SV slot inside a 0x19EB record.

    Byte 0 holds the **GPS PRN** for populated (actively-tracked) slots.
    The 2026-05-27 #N investigation cross-checked RM520N-GL slot[0]
    values against LG290P MSM7 1077 sigid 23 (L5Q) PRN truth and found
    100% overlap on populated slots (run2: 4/4; run3: 2/2).

    **Byte 1 is the populated-slot marker (Phase 2 finding, #N):**
    populated L5 measurement slots carry ``byte[1] == 0x05``. Sentinel
    / stale slots carry ``byte[1] ∈ {0x00, 0x01, 0x02, 0x03, 0x04, 0x08}``
    (one record's histogram across 28 slots showed 7 populated + 21
    sentinel of various flag types). Combined with ``1 ≤ byte[0] ≤ 32``
    this gives a high-precision populated-slot detection rule. On the
    run2 corpus 1,643 populated L5 slots were collected this way.

    **Byte 2 is CN0 in quarter-dB-Hz (Phase 2 finding, #N):**
    ``byte[2] / 4.0 → CN0 in dB-Hz``. Across the run2 corpus:

      | PRN | n_slots | byte[2] mean | derived CN0 | LG290P L5Q truth |
      |-----|--------:|------------: |-----------: |------------------|
      |   6 |    295  |       131.3  |     32.8    |   31.25 dB-Hz    |
      |  11 |    295  |       127.0  |     31.8    |   (not in 1077)  |
      |  14 |    295  |       121.0  |     30.2    |   37.25 (high)   |
      |  24 |    295  |       123.7  |     30.9    |   29.31 dB-Hz    |

    Close-to-exact match on PRN 6 + PRN 24 confirms the CN0 ÷ 4
    encoding (same Qualcomm DIAG convention seen on other GNSS log
    codes). PRN 14's discrepancy (modem 30.2 vs truth 37.25) likely
    reflects epoch offset between the modem capture and the LG290P
    1077 RTCM emission timestamps — across many epochs the per-PRN
    means should track truth more tightly.

    **Byte 3 is the duplicated CN0 value (Phase 3 correction, #N):**
    across the full run2 corpus (1,643 populated slots), ``byte[3] ==
    byte[2]`` holds in **98.0%** of slots, with only 33 exceptions
    spread across PRNs. The Phase 2 docstring claimed "PRNs 1 and 20
    differ" but the corpus walk shows PRN 1 = 238/239 eq, PRN 20 =
    191/192 eq, and PRNs 6/11/14/24 = 100% eq. Hypothesis: byte[2] is
    the primary tracker CN0 and byte[3] is a duplicated/paired CN0
    write (possibly a coherent vs noncoherent estimate that matches in
    steady-state tracking). The rare ``b3 != b2`` cases are preserved
    as ``cn0_b3_quarter`` for downstream RE — likely a divergence
    indicator during signal acquisition transitions.

    **Phase 3 finding (#N) — extensive invariant byte map:**
    A run2 walk across 295 populated PRN 6 slots (one PRN sampled
    across 295 epochs in the gnss_comparison_pocketsdr_2026-05-21_run2
    capture) reveals 30+ invariant bytes inside the 70B slot — i.e.
    the slot has a fixed structural skeleton with measurement data
    concentrated in a few varying ranges:

      Invariant offsets (constant across 295 epochs): 4-6, 10-11, 13,
      17, 29, 33-42, 46, 50-56, 60, 64-65, 66-69. The byte[34..36] =
      ``ff 20 24`` sequence and byte[46] = ``0x40`` + byte[50] =
      ``0x3f`` exponent-bytes suggest little-endian f32 values are
      packed at 4-byte-aligned offsets within the variable regions.

      Variable / measurement-bearing regions: byte[7..9, 12, 14..16,
      18..28, 30..32, 43..45, 47..49, 57..59, 61..63].

    **Phase 3 finding (#N) — f32 candidates for Phase 2b decode:**
    Sweeping 4-byte-aligned f32 unpacks across the 295 PRN 6 populated
    slots surfaces three high-quality candidates with bounded numeric
    behaviour (not random noise):

      | offset | n     | mean        | min          | max         | candidate semantic                |
      |-------:|------:|------------:|-------------:|------------:|-----------------------------------|
      |     8  |  295  | 8.598e+09   |  8.598e+09   | 8.598e+09   | per-PRN-stable large value (PR?)  |
      |    20  |  223  | 1.444e-06   | -2.880e-06   | 1.946e-05   | small signed scalar (phase resid?)|
      |    44  |  207  | -1.711e+04  | -1.968e+05   | 1.201e+01   | wide-span signed (doppler-rate?)  |

    The offset=8 value's per-PRN stability (span 300k units across 295
    epochs of one stationary capture) is consistent with pseudorange-
    scale measurement — GPS pseudoranges drift ~150-500 m over a few
    minutes for a moving satellite. The offset=44 wide span is
    consistent with doppler-rate or phase-residual (signed, large
    magnitude during acquisition, small near tracking lock).

    Full semantic lock requires per-epoch alignment between modem
    record timestamps and LG290P MSM7 1077 emission timestamps; that
    deeper RE is deferred to a Phase 2b follow-up (cite #N Phase 3
    findings as the structural anchor).

    **Phase 2b finding (#N) — Doppler decoded; Phase 3 candidates
    corrected.** A multi-PRN walk (NOT just PRN 6) over all 1,643
    populated run2 slots (23 PRNs) overturns the PRN-6-only Phase 3
    candidate list and locks a real field:

      - **offsets 26 AND 57 carry the same f32** (``off26 - off57 ≈ 0``,
        mean 0.008, |Δ| < 0.12 on PRN 6) — a DUPLICATED value, the same
        redundancy pattern as the byte[2]/byte[3] CN0 pair. Per-PRN it is
        distinct and physically Doppler-shaped: PRN 1 ≈ +580, PRN 6 ≈
        −482, PRN 11 ≈ −723, PRN 14 ≈ +417, PRN 20 ≈ +531, PRN 24 ≈ −205,
        each varying smoothly every epoch (uniq ≈ N). Distinct-per-
        satellite ± hundreds, signed, smooth = **L5 carrier Doppler (Hz)**.
        Decoded as ``doppler_hz`` (@26) + ``doppler_hz_b`` (@57). (The
        value could alternatively be range-rate in m/s — numerically both
        fit; RTCM DF404 truth-correlation would disambiguate, deferred.)
      - **offset 18 is a normalized [0,1] per-epoch f32** (PRN 6: 0.001–
        0.999 uniq=295/295; per-PRN-distinct sub-ranges). Decoded as the
        tentative ``frac_18`` — candidate code-phase fraction or per-SV
        measurement weight; semantic not locked.

      Phase 3's offset 8/20/44 candidates were a PRN-6 OVERFIT: offset 8
      is per-PRN near-constant (~8.6e9, not a per-epoch measurement —
      likely a per-SV reference/code count), and offsets 20 & 44 are
      cross-PRN garbage (f32 swings 1e38 ↔ 1e-36 across PRNs). They are
      NOT decoded. Remaining per-SV-stable f32s at 43/47 (per-PRN
      constants ~0.6–5.1 / ~0.14–0.84) look like per-SV calibration/bias,
      left undecoded.

    **Phase 3 finding (#N) — `20 00 02 00` marker is a SENTINEL feature:**
    The Phase 2 docstring noted a 4-byte ``20 00 02 00`` marker followed
    by two f32 values. The Phase 3 corpus walk reframes its semantic:
    the marker appears at slot offset 36 in **11.0% of sentinel slots**
    (723 / 6,590) but only **0.2% of populated slots** (3 / 1,643).
    It is a sentinel-slot decoration (likely a stale-tracker tag), not
    a populated-slot field. Phase 2b decode should NOT key on this
    marker for populated-slot pseudorange / doppler extraction.

    Full 70B preserved as ``raw`` for downstream cross-correlation.
    """
    prn: int              # u8  @  0 — GPS PRN for populated slots; sentinel otherwise
    valid_flag: int       # u8  @  1 — populated-slot marker; 0x05 = populated L5 measurement
    cn0_quarter_db: int   # u8  @  2 — CN0 × 4 in quarter-dB-Hz units (primary tracker)
    cn0_b3_quarter: int   # u8  @  3 — secondary CN0 candidate (Phase 2b RE)
    populated: bool       # derived — True iff valid_flag == 0x05 and 1 <= prn <= 32
    cn0_db_hz: float | None  # derived — cn0_quarter_db / 4.0 when populated, else None
    doppler_hz: float | None    # f32 @ 26 — L5 carrier Doppler (Phase 2b, #N); populated only
    doppler_hz_b: float | None  # f32 @ 57 — duplicated Doppler (== doppler_hz; mirrors cn0 b2/b3)
    frac_18: float | None       # f32 @ 18 — normalized [0,1] per-epoch field (tentative); populated only
    raw: bytes            # full 70B slot payload


