def parse_0x19eb(log_time: int, data: bytes) -> Diag0x19EB | None:
    n = len(data)
    if n < 32 + 70:  # need header + at least one entry
        return None
    version = data[0]
    if version != 0x01:
        return None
    body_len = n - 32
    if body_len % 70 != 0:
        return None
    entry_count = body_len // 70
    entries = []
    for i in range(entry_count):
        off = 32 + i * 70
        e_bytes = data[off:off + 70]
        prn = e_bytes[0]
        valid_flag = e_bytes[1]
        cn0_q = e_bytes[2]
        cn0_b3 = e_bytes[3]
        populated = (valid_flag == _VALID_FLAG_POPULATED) and (1 <= prn <= 32)
        if populated:
            cn0_db_hz = cn0_q / 4.0
            doppler_hz = unpack_from('<f', e_bytes, _OFF_DOPPLER)[0]
            doppler_hz_b = unpack_from('<f', e_bytes, _OFF_DOPPLER_B)[0]
            frac_18 = unpack_from('<f', e_bytes, _OFF_FRAC18)[0]
        else:
            cn0_db_hz = doppler_hz = doppler_hz_b = frac_18 = None
        entries.append(Gnss19EBL5Entry(
            prn=prn,
            valid_flag=valid_flag,
            cn0_quarter_db=cn0_q,
            cn0_b3_quarter=cn0_b3,
            populated=populated,
            cn0_db_hz=cn0_db_hz,
            doppler_hz=doppler_hz,
            doppler_hz_b=doppler_hz_b,
            frac_18=frac_18,
            raw=e_bytes,
        ))
    return Diag0x19EB(
        log_time=log_time,
        version=version,
        raw_header=bytes(data[:32]),
        entry_count=entry_count,
        entries=entries,
        payload_size=n,
    )
