# Phase 2b per-slot measurement-field offsets (#N). Cross-PRN verified on
# the run2 corpus (1,643 populated slots, 23 PRNs):
#   - offsets 26 & 57 carry the SAME f32 (off26 - off57 ≈ 0), per-PRN-distinct
