def parse_0x4179(log_time: int, data: bytes) -> Diag0x4179 | None:
    if len(data) < _TAIL_BYTES + 1:
        return None
    version = data[0]
    if version not in _SUPPORTED_VERSIONS:
        return None
    base = _BASE_SIZE_FOR_VERSION[version]
    sz = len(data)
    header_size = base - _TAIL_BYTES  # 51 for v=0x05, 29 for v=0x08
    layout = _LAYOUT[version]
    prefix_size = layout["prefix"]
    gap_size = layout["gap"]
    stride = layout["stride"]
    trailer_size = layout["trailer"]
    c0 = layout["c0"]
    blkcount_off = layout["blkcount_off"]
    # Sanity: any payload below the base size for its version is malformed.
    if sz < base:
        return None
    # arrayA + arrayB always live in the trailing 48 bytes.
    array_a_off = sz - _TAIL_BYTES
    array_b_off = sz - _ARRAY_BYTES
    array_a_raw = list(unpack_from('<6I', data, array_a_off))
    array_b_raw = list(unpack_from('<6I', data, array_b_off))

    earfcn_or_freq: list[int | None] = []
    measurement_raw: list[int | None] = []
    measured_cell_count = 0
    for a, b in zip(array_a_raw, array_b_raw):
        if b == 0:
            # arrayB zero -> unmeasured slot (validated by monotonic
            # zero-count progression across slots 0->5 in the corpus).
            earfcn_or_freq.append(None)
            measurement_raw.append(None)
        else:
            earfcn_or_freq.append(a)
            measurement_raw.append(b)
            measured_cell_count += 1

    header_raw = bytes(data[:header_size])
    middle_raw = bytes(data[header_size:array_a_off])

    # --- block_count + prefix decode (#N 2026-06-26) --------------------
    # On-formula (stacked-preamble) records satisfy sz == c0 + stride*B. The
    # firmware also states B in the prefix (v=0x08 @12, v=0x05 @26); the two
    # must agree -> block_count_mirror_ok. base+48 prev-window records and
    # any off-formula size leave block_count from the prefix byte (mirror
    # N/A -> False) without disturbing the canonical decode.
    on_formula = (sz - c0) % stride == 0 and sz >= c0 + stride
    block_count_from_size = (sz - c0) // stride if on_formula else None
    block_count_from_prefix = data[blkcount_off] if sz > blkcount_off else None
    block_count = (
        block_count_from_size
        if block_count_from_size is not None
        else (block_count_from_prefix if block_count_from_prefix else 1)
    )
    block_count_mirror_ok = (
        block_count_from_size is not None
        and block_count_from_prefix == block_count_from_size
    )

    prefix_raw = bytes(data[:prefix_size])
    seq_u16 = unpack_from('<H', data, 3)[0] if sz >= 5 else None
    # Named prefix fields are the v=0x08 decode; on v=0x05 the offsets differ
    # so only version + block_count are pinned (rest stays in prefix_raw).
    if version == 0x08:
        camp_context_u32: int | None = unpack_from('<I', data, 8)[0] if sz >= 12 else None
        prefix_flag_a: int | None = data[2] if sz > 2 else None
        prefix_flag_b: int | None = data[13] if sz > 13 else None
        # v=9: name the fixed prefix constants (RM520N-GL 28/28). These are the
        # last previously-unnamed prefix bytes; prefix_const_ok is a soft drift
        # witness (records presence, never rejects -- see dataclass docstring).
        sub_format: int | None = data[1] if sz > 1 else None
        format_const_raw = bytes(data[5:8]) if sz >= 8 else b""
        prefix_tag: int | None = data[14] if sz > 14 else None
        prefix_const_ok = (
            sub_format == 0x01
            and format_const_raw == b"\x00\x02\x00"
            and prefix_tag == 0x01
        )
    else:
        camp_context_u32 = None
        prefix_flag_a = None
        prefix_flag_b = None
        sub_format = None
        format_const_raw = b""
        prefix_tag = None
        prefix_const_ok = False

    # All B preambles, at the CORRECTED contiguous offset prefix_size + i*11
    # (the #N alignment fix: was header_size, which absorbed the first
    # preamble into header_raw). Only meaningful for on-formula records; for
    # off-formula / base+48 forms we leave it empty rather than mis-slice.
    preambles: list[dict[str, Any]] = []
    gap_raw = b""
    if on_formula:
        for i in range(block_count):
            off = prefix_size + i * _PREAMBLE_SIZE
            preambles.append(_decode_preamble(data[off:off + _PREAMBLE_SIZE]))
        gap_off = prefix_size + block_count * _PREAMBLE_SIZE
        gap_raw = bytes(data[gap_off:gap_off + gap_size])

    # v=3: try prior-window Block48 on base+48 records. Gate on the same
    # invariants the tail satisfies; gate-fail leaves prev_* unpopulated
    # so the regression mode is "no extra fields" rather than "wrong
    # extra fields" (size-vs-format invariance, core memory).
    prev_earfcn_or_freq: list[int | None] = []
    prev_measurement_raw: list[int | None] = []
    prev_measured_cell_count: int | None = None
    array_a_prev_raw: list[int] = []
    array_b_prev_raw: list[int] = []
    if len(middle_raw) == _TAIL_BYTES:
        cand_a = list(unpack_from('<6I', middle_raw, 0))
        cand_b = list(unpack_from('<6I', middle_raw, _ARRAY_BYTES))
        if _block48_valid(cand_a, cand_b):
            array_a_prev_raw = cand_a
            array_b_prev_raw = cand_b
            prev_measured_cell_count = 0
            for a, b in zip(cand_a, cand_b):
                if b == 0:
                    prev_earfcn_or_freq.append(None)
                    prev_measurement_raw.append(None)
                else:
                    prev_earfcn_or_freq.append(a)
                    prev_measurement_raw.append(b)
                    prev_measured_cell_count += 1

    # v=4: multi-layer per-carrier block decode for sz > base+48 where
    # (sz - base) % stride == 0. Layout per version params is N preambles
    # stacked, then N (Block48 + trailer) pairs, then tail Block48 (already
    # decoded above as arrayA/arrayB). Atomically populate `blocks` only
    # when EVERY Block48 passes the invariant gate; any single gate-fail
    # leaves blocks=[] so a future-firmware layout drift surfaces as
    # "no extra fields" instead of a half-populated structure.
    blocks: list[Diag4179Block] = []
    if sz > base + _TAIL_BYTES and version in _MULTI_LAYER_PARAMS:
        preamble_size, trailer_size, stride = _MULTI_LAYER_PARAMS[version]
        body_delta = sz - base
        if body_delta % stride == 0:
            n_layers = body_delta // stride
            # CORRECTED preamble offset (#N alignment fix): preambles are
            # contiguous from prefix_size, NOT header_size. blocks_region_off
            # is unchanged (header_size + n_layers*11 == prefix_size + 11*B +
            # gap), so the validated Block48 offsets do not move.
            preamble_region_off = prefix_size
            blocks_region_off = header_size + n_layers * preamble_size
            block_stride = _TAIL_BYTES + trailer_size
            candidates: list[Diag4179Block] = []
            all_gates_pass = True
            for i in range(n_layers):
                pre_off = preamble_region_off + i * preamble_size
                blk_off = blocks_region_off + i * block_stride
                a_off = blk_off
                b_off = blk_off + _ARRAY_BYTES
                t_off = blk_off + _TAIL_BYTES
                cand_a = list(unpack_from('<6I', data, a_off))
                cand_b = list(unpack_from('<6I', data, b_off))
                if not _block48_valid(cand_a, cand_b):
                    all_gates_pass = False
                    break
                preamble_bytes = bytes(data[pre_off:pre_off + preamble_size])
                pre_dec = _decode_preamble(preamble_bytes)
                trailer_bytes = bytes(data[t_off:t_off + trailer_size])
                blk_earfcn: list[int | None] = []
                blk_meas: list[int | None] = []
                blk_count = 0
                for a, b in zip(cand_a, cand_b):
                    if b == 0:
                        blk_earfcn.append(None)
                        blk_meas.append(None)
                    else:
                        blk_earfcn.append(a)
                        blk_meas.append(b)
                        blk_count += 1
                candidates.append(Diag4179Block(
                    preamble_raw=preamble_bytes,
                    array_a_raw=cand_a,
                    array_b_raw=cand_b,
                    trailer_raw=trailer_bytes,
                    earfcn_or_freq=blk_earfcn,
                    measurement_raw=blk_meas,
                    measured_cell_count=blk_count,
                    preamble_value=pre_dec["value"],
                    preamble_marker_ok=pre_dec["marker_ok"],
                    preamble_const_ok=pre_dec["const_ok"],
                    preamble_is_final=pre_dec["is_final"],
                ))
            if all_gates_pass:
                blocks = candidates

    # v=9: record-level witness that every non-tail block trailer is all-zero
    # (observed 75/75 RM520N-GL blocks). Vacuously True for base records (B=1,
    # no non-tail blocks); a future non-zero trailer flips it without disturbing
    # the canonical decode.
    block_trailers_zeroed = all(
        all(byte == 0 for byte in blk.trailer_raw) for blk in blocks
    )

    return Diag0x4179(
        log_time=log_time,
        version=version,
        payload_size=sz,
        header_size=header_size,
        block_count=block_count,
        block_count_mirror_ok=block_count_mirror_ok,
        prefix_raw=prefix_raw,
        seq_u16=seq_u16,
        camp_context_u32=camp_context_u32,
        prefix_flag_a=prefix_flag_a,
        prefix_flag_b=prefix_flag_b,
        sub_format=sub_format,
        format_const_raw=format_const_raw,
        prefix_tag=prefix_tag,
        prefix_const_ok=prefix_const_ok,
        block_trailers_zeroed=block_trailers_zeroed,
        preambles=preambles,
        gap_raw=gap_raw,
        earfcn_or_freq=earfcn_or_freq,
        measurement_raw=measurement_raw,
        measured_cell_count=measured_cell_count,
        array_a_raw=array_a_raw,
        array_b_raw=array_b_raw,
        header_raw=header_raw,
        middle_raw=middle_raw,
        prev_earfcn_or_freq=prev_earfcn_or_freq,
        prev_measurement_raw=prev_measurement_raw,
        prev_measured_cell_count=prev_measured_cell_count,
        array_a_prev_raw=array_a_prev_raw,
        array_b_prev_raw=array_b_prev_raw,
        blocks=blocks,
    )
