def _block48_valid(array_a: list[int], array_b: list[int]) -> bool:
    """Return True iff (array_a, array_b) satisfy the Block48 invariants
    the tail arrays exhibit corpus-wide:

    - arrayB non-strictly descending across all 6 slots (trailing-zero-
      fill is naturally descending, so this also covers the "fewer than
      6 measured cells" case).
    - arrayA byte-3 == 0 AND byte-2 in {0..4} on every slot whose
      paired arrayB value is non-zero (i.e. populated cells).

    Both invariants were verified 139/139 on the wardriving corpus's
    base+48 records (#N <redacted-ref>).
    """
    if not all(array_b[i] >= array_b[i + 1] for i in range(5)):
        return False
    for a, b in zip(array_a, array_b):
        if b == 0:
            continue
        if (a >> 24) != 0:
            return False
        if ((a >> 16) & 0xff) > 4:
            return False
    return True


def _decode_preamble(pre: bytes) -> dict[str, Any]:
    """Decode one 11-byte preamble: marker(3) + value(u32 LE) + const(3) +
    delim(1). Returns a dict with the raw bytes, the per-block value, the
    two literal-presence witnesses, and the ``is_final`` delim flag
    (delim == 0 marks the tail block). Tolerant of short/garbage slices
    (returns presence=False rather than raising) so a future-firmware
    layout drift degrades to "literals absent" instead of a parse failure.
    """
    if len(pre) < _PREAMBLE_SIZE:
        return {
            "preamble_raw": bytes(pre),
            "value": 0,
            "marker_ok": False,
            "const_ok": False,
            "is_final": False,
        }
    value = int.from_bytes(pre[3:7], "little")
    return {
        "preamble_raw": bytes(pre),
        "value": value,
        "marker_ok": pre[0:3] == _PRE_MARKER,
        "const_ok": pre[7:10] == _PRE_CONST,
        "is_final": pre[10] == 0,
    }


