# --- Full record layout spec, per version (#N structural RE 2026-06-26) --
#
# A v=4 multi-block 0x4179 record is, byte-for-byte::
#
#     prefix [prefix_size]                         # fixed front matter
#     B × preamble [11]                            # one per measurement block
#     gap [gap_size]                               # constant 0x00 fill
#     (B-1) × (Block48[48] + trailer[trailer_size])# non-tail measurement layers
#     tail Block48 [48]                            # the canonical "current" window
#
# where ``B`` = block_count = the number of 6-cell measurement blocks. The
# whole record obeys ``sz == prefix_size + gap_size + 48 + 11*B +
# (B-1)*(48+trailer_size)`` which collapses to ``sz == c0 + stride*B`` with
# ``stride = 11 + 48 + trailer_size`` and ``c0 = prefix_size + gap_size``.
# Empirically (31 RM520N-GL v=0x08 records + 209 LM960 v=0x05 records,
# 2026-06-26): v=0x08 ``sz = 14 + 63*B``; v=0x05 ``sz = 28 + 71*B``.
#
# ``block_count`` is *also stated in the prefix* (v=0x08 @ byte 12, v=0x05 @
# byte 26), giving an independent count-mirror invariant against the size
# arithmetic — a drift guard the prior size-only parser lacked.
#
# Each 11-byte preamble decomposes as::
#
#     marker [3]  = 23 00 00                  (literal, present in every block)
#     value  [4]  = per-block u32 LE          (unique-per-block, counter/ts-shaped)
#     const  [3]  = c0 00 04                   (literal)
#     delim  [1]  = 01 for every block EXCEPT the last (tail) block, which = 00
#                   -> a self-describing "final block" terminator that
#                      independently corroborates block_count.
#
# IMPORTANT (the #N preamble-alignment fix): preambles start at
# ``prefix_size`` (15 on v=0x08, 29 on v=0x05) and are *contiguous* — NOT at
# the old ``header_size`` (29 / 51) the prior parser used, which absorbed the
# first preamble into ``header_raw`` and left the per-block ``preamble_raw``
# off by one layer. The Block48 measurement offsets are UNCHANGED by this fix
# (``prefix_size + 11*B + gap_size == header_size + 11*(B-1)``), so the
# 224/224 + 80/80-validated arrayA/arrayB decode is preserved exactly.
#
# Fields: prefix_size, blkcount_off, gap_size, trailer_size, stride, c0, base.
