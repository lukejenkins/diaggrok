# 0x188B — GNSS reference-position cache (257B MDM9x07/SDX20, 287B SDX55) (#N)
# ---------------------------------------------------------------------------
# Two distinct record variants exist:
#
#   - `v0_257B_mdm9x07_sdx20` — version=0, size=257. Observed on MDM9x07
#     (EP06A, EG18-NA) and SDX20 (LM960). 2896 records total across the
#     2026-04-20 corpus. 190/257 bytes constant; 6 coordinate-pair repeats
#     at [4:12], [22:30], [130:138], [166:174], [220:228], [238:246], all
#     decoding to the receiver's known static position (41.22°N, -111.94°W
#     = WirelessHaven lab). A Qualcomm-default second reference pair at
#     [40:48] decodes to (38.0000°N, -117.0000°W) constant across all
#     chipsets.
#
#   - `v2_287B_sdx55` — version=2, size=287. Observed only on FN980m
#     (SDX55) in the 2026-04-20 corpus (8 records from GNSS lg290p
#     comparison capture). 265/287 bytes constant (92.3% constant). The
#     record structure is **entirely different** from the 257B form —
#     the old hypothesis that it was a "30-byte extension" of the v0
#     format is falsified: byte-0 differs, ref-pair repeats are at
#     [4], [166], [226], [246] (4 copies, not 6), and the Qualcomm-default
#     (38N/-117W) pair at [40:48] is absent.
#
#     Critical new finding: v2/287B stores the receiver's reference
#     position in BOTH radians AND decimal degrees:
#       [4:8]   = f32 ref_lat_rad = 0.71934    (41.22°N in radians)
#       [8:12]  = f32 ref_lon_rad = -1.95366   (-111.94°W in radians)
#       [24:28] = f32 ref_lat_deg = 41.2151
#       [28:32] = f32 ref_lon_deg = -111.936
#     The 257B variant has no decimal-degree fields.
#
# **2026-04-21 RE update (1260 v2/287B records, up from 8):** extending
# the corpus to 5 additional FN980m captures (TMO / Verizon / ATT /
# generic / cold-reset) revealed that v2/287B is NOT a static cache —
# more than half the "constant" bytes identified in the 8-record pilot
# are actually per-record varying measurement fields.  Key decoded
# fields now:
#
#       [12:16] f32  `altitude_ref_m`       — cached reference altitude
#                                             (1395..1435 m, matches
#                                              SLC-area lab elevation)
#       [32:36] f32  `altitude_fix_m`       — per-record current fix
#                                             altitude (~1240 distinct
#                                              values across 1260 rec)
#       [36:40] f32  `dop_h_like`           — drops from ~12 to ~7 during
#                                             fix acquisition; HDOP-shape
#       [40:44] f32  `dop_v_like`           — drops from ~12 to ~4;
#                                             VDOP-shape (rises on GNSS-
#                                             denied / indoor conditions)
#
# The four `(lat_rad, lon_rad)` repeats at [4], [166], [226], [246] each
# show slight per-record variance (5th/6th decimal place) — this is a
# short rolling history of recent fix lat/lon, not four copies of a
# static cache.
#
# v0/257B field map (condensed):
#     [0:2]  u16LE  version            = 0x0000 (CONSTANT)
#     [2]    u8     gen_marker         (0x87 EP06A/LM960, 0x8f EG18-NA)
#     [3]    u8     flag               = 0x02 (CONSTANT)
#     [4:8]  f32LE  ref_lat_rad        — receiver latitude (rad)
#     [8:12] f32LE  ref_lon_rad        — receiver longitude (rad)
#     [40:44] f32LE qcom_ref_lat_rad   = 0x3F800000 (Qualcomm default)
#     [44:48] f32LE qcom_ref_lon_rad   = Qualcomm default
#     [256]  u8     trailer            = 0x01 (CONSTANT)
#
# v2/287B field map (condensed):
#     [0:2]  u16LE  version            = 0x0002 (CONSTANT)
#     [2]    u8     gen_marker         = 0x87 or 0x8f (varies 135/143)
#     [3]    u8     flag               = 0x00 (CONSTANT)
#     [4:8]  f32LE  ref_lat_rad        — receiver latitude (rad)
#     [8:12] f32LE  ref_lon_rad        — receiver longitude (rad)
#     [12:16] f32LE altitude_ref_m     — cached reference altitude (m)
#     [16:20] f32LE measurement_16     — ~20053..20058 (slowly drifts;
#                                        possibly ionosphere delay or
#                                        clock-drift scalar)
#     [20:24] f32LE measurement_20     — ~5232..5299 (slowly drifts)
#     [24:28] f32LE ref_lat_deg        — receiver latitude (decimal deg)
#     [28:32] f32LE ref_lon_deg        — receiver longitude (decimal deg)
#     [32:36] f32LE altitude_fix_m     — per-record current fix altitude
#     [36:40] f32LE dop_h_like         — HDOP-like (range 7..13)
#     [40:44] f32LE dop_v_like         — VDOP-like (range 3..12)

