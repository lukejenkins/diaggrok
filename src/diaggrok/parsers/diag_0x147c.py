_V0D_SV_SIG = b'\x00\x00\x75\x11\x01'  # 5-byte cross-capture-stable SV-record signature

# Cross-capture preamble invariants — the 17 bytes that hold across every
# v=0x0D capture surveyed (20 captures × 30 records each = 600 records
# spanning normal, warm-restart, reboot, SIM-power-cycle, and airplane-mode
# operational states). Bytes that are "mostly constant" but change during
# state transitions (4, 8, 15, 16, 17, 21, 32) are deliberately EXCLUDED
# from this invariant set — they're per-session firmware-state and would
# misleadingly fail otherwise-valid records during boot.
#
