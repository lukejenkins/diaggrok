#     [0:4]    u32 LE  tick_a  — GNSS-engine clock tick, +256,000 / record.
#                               byte[0] is pinned at 0x0A so it doubles as
#                               the version gate (256000 % 256 == 0, so the
#                               low byte never changes).
#     [4:8]    u32 LE  tick_b  — second clock tick, also +256,000 / record
#                               (byte[4] pinned 0x00). Matches the same GNSS-
#                               engine cadence the v=0x0D body shows.
#     [77:79]  u16 LE  state_x — per-record SEQUENCE COUNTER, strictly +1 each
#                               record (0,1,2,…). Verified offline against the
#                               gpsd_ref ground truth, #N <redacted-ref>: all
#                               1132 deltas == 1 over the canonical EM9190 walk.
#                               The earlier "77-78: 256/5-uniq status region" is
#                               just this counter's full-cycling low byte + the
#                               high byte (0..4 over 1133 records) — NOT a flag.
#     [97:100] u24 LE  state_y — monotonic engine tick, +~467 / record (range
#                               462..479, dominant 463/464). A distinct, slower
#                               cadence from tick_a/tick_b (+256000/rec) — NOT a
#                               status field. Same #N offline walk.
#
