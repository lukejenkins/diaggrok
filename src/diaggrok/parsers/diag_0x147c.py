# 0x147C — GNSS PE WLS Position Report (per-SV measurement cache)
# ---------------------------------------------------------------------------
#
# Canonical name LOG_GNSS_PE_WLS_POSITION_REPORT_C tells us this is the
# **Position-Engine Weighted-Least-Squares per-SV measurement contributions**,
# NOT raw subframe-derived broadcast ephemeris (the issue body's RINEX-NAV
# framing was an aspirational scope; this code carries PE inputs/outputs,
# not the Keplerian set the RINEX .nav format wants — that would need a
# different log code).
#
# v=0x0D (SDX62 RM520N-GL) — body decode landed v4 (#N <redacted-ref>):
#
# Layout: [44B preamble] + [N × 47B slot grid]
#     The slot grid splits into two classes:
#       - "head slots"  : 15 slots (705 B) of reserved/template space with
#                         session-state floats embedded at fixed positions
#                         (the rest is filled with a snake-aligned
#                         `00000000_4912620f` u64 padding constant — likely
#                         an allocator's per-8B fill marker)
#       - "SV records"  : one 47-byte record per actively-tracked SV
#
# Verified across 11 distinct payload sizes (796..1407 B) on 1,842 records
# from `<redacted-pii>`: every size satisfies
# `(size - 44) % 47 == 0` exactly.  For size classes 937..1125 (1,585 / 1,842
# records = 86%), `head_slots` is locked at exactly 15; 1219+ sizes have a
# variable head (18..22 slots) — likely state-transition or different
# constellation pages.  The 796B class (238 records) is a no-SV-tracked
# state — preamble follows the same template but with session-variable
# bytes in different positions.
#
# Per-SV record layout (47 bytes, cross-capture stable):
#     [0:5]    `00 00 75 11 01`     5-byte signature
#     [5]      0x00                 reserved
#     [6]      u8                   PRN (GPS SV ID; 1..32)
#     [7]      0x00                 reserved
#     [8:10]   `00 07`              record-type marker (LE u16 = 0x0700)
#     [10]     u8                   subframe-type / status flags (per-SV
#                                   constant across consecutive records;
#                                   varies between PRNs — likely IODE or
#                                   tracking-channel state)
#     [11:15]  f32 LE  wls_f1       per-SV measurement; range ±10
#     [15:19]  f32 LE  wls_f2       per-SV measurement; range [2..16),
#                                   often exactly 5.0 (likely constellation
#                                   or # of tracked frequencies)
#     [19:23]  f32 LE  wls_f3       per-SV measurement; range [0.1..1.0],
#                                   often exactly 1.0 (validity weight?)
#     [23:27]  f32 LE  wls_f4       per-SV measurement; range [0.05..1.0]
#                                   (normalized elevation cosine?)
#     [27:31]  f32 LE  wls_f5       per-SV measurement; range [600..30000]
#                                   (likely a measurement magnitude in m)
#     [31:35]  f32 LE  wls_f6       per-SV measurement; range [10..5000]
#                                   (likely a measurement variance)
#     [35:39]  f32 LE  wls_f7       per-SV measurement; range ±0.02
#                                   (small residual; Doppler-rate-like)
#     [39:43]  f32 LE  wls_f8       per-SV measurement; range [0.01..0.6]
#                                   (per-PRN tight range — possibly C/N0
#                                   noise-floor quantization step)
#     [43:47]  f32 LE  wls_f9       per-SV measurement; range [0.7..1.0],
#                                   usually exactly 1.0 (validity flag)
#
# Field names are deliberately generic (wls_f1..wls_f9) pending semantic
# RE — the magnitudes and ranges are documented above but assigning RINEX/
# GPS-ICD names to them requires cross-correlation with a known-position
# fix and per-SV LOS computation. The structural layout is locked.
#
# 44B preamble — variance pass on 1,136 records of one capture vs cross-
# capture comparison vs a 2nd RM520N-GL capture yields these invariants:
#
#     Cross-capture CONSTANTS (= 22 of 44 bytes):
#         [0]      0x0D                 version byte (matches v=0x0D enum)
#         [4]      0x00                 separator
#         [8:13]   00 00 00 00 00       reserved
#         [15]     0x0F                 magic byte (low byte of session magic)
#         [16:21]  00 00 00 00 00       reserved
#         [21]     0x04                 sub-version / field-count marker
#         [22:32]  00 ... 00 (10 B)     reserved
#         [32]     0x72                 magic byte
#
#     SESSION-stable (constant within session, varies across sessions):
#         [13:15], [33], [42:44] — firmware-state magic bytes
#
#     VARIABLE (per-record):
#         [1:4]    u24 LE counter (increments by ~1000 per record)
#         [5:7]    u16 LE counter (related)
#         [34:42]  session-state floats / TOW
#
# Other versions (v=0x06, 0x07, 0x08, 0x0A) — body still opaque pending
# their own RE pass; the layer-1 version gate is preserved and
# `wls_records` is left empty on those versions until per-version RE lands.
#
# v=0x0A variance map (<redacted-ref> iter2 finding, #N):
#
#   Cross-vendor per-byte variance pass over EM9190 SWIX55C 1133 records
#   (4641 B fixed) revealed **99.4% of body bytes are LOCKED** (only 29
#   varying offsets in the EM9190 sample). FN980m SDX55 (1166 records)
#   has only 11 varying offsets, ALL of which are a strict subset of
#   EM9190's 29. The 11 common varying positions are the cross-vendor
#   universal-format core:
#
#     1, 2, 3      — counter triplet (byte 1: 32-unique bitfield with
#                     `x & 0x07 == 7`; byte 2: full-range 256 unique;
#                     byte 3: narrow 18-unique slow counter)
#     5, 6, 7      — mirror of 1-3 (related counter pair)
#     77, 78       — 256/5-unique 2-byte region
#     97, 98, 99   — 256/256/9-unique 3-byte region
#
#   Locked f32 anchor at body offset [41:44] = `59 fc 5a 46` (f32 LE =
#   ~14015) appears EXACTLY ONCE in the full 4641 B body. The "3 slots
#   at 30/51/61" pattern from the prior <redacted-ref>-05-27 recon is
#   **EM9190-specific** (not seen on FN980m at all) — likely a Sierra
#   vendor extension, not core 0x147C v=0x0A structure.
#
#   EM9190-only varying offsets: 30-32, 51-53, 61-63, 488, 490, 502-503,
#   506, 547-548, 552, 554. The 488-554 region's 0/1 toggle pattern
#   looks like state flags or SV-availability bitmaps.
#
#   **Falsified prior hypothesis**: 0x147C v=0x0A is NOT a per-SV
#   measurement record with 10/11/13 B per-SV slots. The 4641 B size
#   is overwhelmingly zero-padded reserved space, not a slotted array.
#   The actual semantic content occupies ~29 bytes (EM9190) or ~11
#   bytes (FN980m) — the rest is template padding.
#
#   Next-step recipe for the next session that picks up this body
#   decode: focus on the 11 universal-format positions first
#   (cross-vendor stable). Decode the 1-3 / 5-7 dual-counter region as
#   "sub-tick counter + slow coarse counter" pair (matches the GNSS
#   engine 256,000-per-record cadence the v=0x0D body also shows).
#   The 77-99 region's small uniq count suggests status flags rather
#   than a measurement. Then per-vendor extensions can be layered on
#   top once the core decode is locked.

# ---- v=0x0D structural constants -----------------------------------------

