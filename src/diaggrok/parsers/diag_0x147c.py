#
# v=0x0A (SDX55: EM9190 / FN980 / FN980m / sim8202g-m2 / RM500Q-AE) is a
# FIXED 4641-byte sparse GNSS-engine state record — NOT a per-SV slot array
# (that hypothesis was falsified; see the variance-map comment above). Two
# recon sessions + this session's cross-vendor walk (EM9190 1,133 recs +
# FN980 1,166 recs, sequential per the <redacted-ref> memory-safety rule) established
# that only 11 byte positions carry cross-vendor-universal semantic content;
# the rest is template padding or per-firmware constants.
#
