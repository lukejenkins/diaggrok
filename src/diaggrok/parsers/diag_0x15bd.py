def parse_0x15bd(log_time: int, data: bytes) -> Diag0x15BD | None:
    if len(data) < 16:
        return None
    if data[0] not in (2, 3):
        return None
    v2_sub_records: list[V2SubRecord] = []
    v3_sub_records: list[V3SubRecord] = []
    if data[0] == 2 and len(data) >= 5 + 28:
        v2_sub_records = _decode_v2_sub_records(data[5:])
    elif data[0] == 3 and len(data) >= 9 + 28:
        v3_sub_records = _decode_v3_sub_records(data[9:])
    return Diag0x15BD(
        log_time=log_time,
        version=data[0],
        sub_type=data[1],
        counter=data[2],
        byte_3=data[3],
        record_type_tag=data[4],
        header_tick=unpack_from('<I', data, 5)[0],
        payload_size=len(data),
        body_raw=data[16:],
        v2_sub_records=v2_sub_records,
        v3_sub_records=v3_sub_records,
    )
