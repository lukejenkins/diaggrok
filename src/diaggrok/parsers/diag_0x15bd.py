@dataclass
class V3SubRecord:
    """Per-sub-record fields for 0x15BD v=3 payloads (modern chipsets).

    Each sub-record is 28 bytes. 10 sub-records per 289-byte v=3 payload,
    starting at payload offset 9. Two kinds coexist in a single payload,
    discriminated by byte [7] (``kind_tag``):

    - **Kind A** (``kind_tag == 0x57``): full record with ``aux_u16`` and
      ``aux_byte`` populated. Bytes [6]=0x0f, [25]=0x01, [27]=0x01 also
      mark kind A (redundant encoding, verified as invariants).
    - **Kind B** (``kind_tag == 0x00``): compact record with auxiliary
      bytes zero. Bytes [6]=0x00, [25]=0x0d, [27]=0x00.

    Empirical layout from 1250 sub-records across 125 v=3 records
    (em7511 MDM9650 × 115, eg18na SDX20 V2 × 3, ep06a MDM9x07 × 7):

    ```
    off  type   name                notes
     0..3  u32LE  tick                full 32-bit counter, monotonically
                                       increasing across the 10 sub-records of
                                       every payload.  Earlier RE (2026-04-22)
                                       claimed "24-bit in practice (byte[3]
                                       always 0)" based on a 1250-sub-record
                                       sample where the modem had been up
                                       <16M ticks (~93 min at 3 kHz) — but
                                       extended-uptime captures (em7511
                                       edge_cases × 5 dirs / 108 records;
                                       wardriving 2026-04-24 ec25/eg25g
                                       correlate / 94 records) do show
                                       byte[3] != 0.  Treat tick as a full
                                       u32. [#N RTCM revisit, <redacted-ref>]
     4..5  u16LE  aux_u16             kind A only; 0 in kind B.  On kind A
                                       the high byte is in {0xBF, 0xC0, 0xC1}
                                       with low byte varying — looks like a
                                       second-dimension index/count
     6     u8     kind_marker_6       0 in kind B, 0x0F in kind A
     7     u8     kind_tag            0 in kind B, 0x57 in kind A (primary
                                       discriminator)
     8..11 4B     reserved_zeros      always 0
    12..13 u16LE  field_x             slowly-varying quantity shared across
                                       sub-record groups (typical range
                                       25700..28400 across chipsets — looks
                                       like an epoch/slot index that evolves
                                       over the payload)
    14..15 2B     reserved_zeros      always 0
    16..17 i16LE  field_y             small signed; narrow range per chipset
                                       (em7511: ~-870; ep06a: ~-20; eg18na:
                                       ~2160)
    18..19 i16LE  field_z             small signed; also narrow per chipset
    20..23 4B     reserved_zeros      always 0
    24     u8     end_tag_a           constant 1
    25     u8     end_tag_b           0x01 in kind A, 0x0D in kind B
    26     u8     aux_byte            kind A only (non-zero); 0 in kind B
    27     u8     kind_marker_27      1 in kind A, 0 in kind B
    ```

    Kinds appear interleaved within a payload (observed pattern is not
    strictly periodic — the em7511 corpus shows AABBABBABB, AAABBAAABB, etc.
    across adjacent records).  The tick is strictly monotonic across ALL
    sub-records regardless of kind.

    Semantic interpretation is not yet locked.  Candidates: kind A could be
    a "tracking checkpoint" (with richer aux fields) while kind B is a
    "delta update" between checkpoints (note tick delta within a kind B
    pair is typically 6-15, far smaller than the kind A → A delta of 1000+).
    Further RE would benefit from tick-vs-wallclock alignment with a
    correlated AT-layer GPS log.
    """
    tick: int
    kind: int         # byte [7]: 0x00 (kind B) or 0x57 (kind A)
    aux_u16: int
    field_x: int
    field_y: int
    field_z: int
    aux_byte: int

    def to_dict(self) -> dict[str, Any]:
        return {
            'tick': self.tick,
            'kind': self.kind,
            'aux_u16': self.aux_u16,
            'field_x': self.field_x,
            'field_y': self.field_y,
            'field_z': self.field_z,
            'aux_byte': self.aux_byte,
        }


