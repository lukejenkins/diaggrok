@register(LOG_GNSS_GAL_MEASUREMENT_REPORT_C,
    name="0x1886",
    issues=(),
    description="Galileo E1/L1 measurement report; SDX55 full 811-byte format with 10 SV slots (gated on the FN980m flags1=0x02c02006 slot signature, #N) + legacy 30-byte indoor format. v4 adds 'sdx55_811_unrecognized' for 811-byte records whose slot layout diverges from the validated FN980m layout, preventing silent garbage SV decode.",
    version=4,
    author="Luke Jenkins",
    author_url="https://github.com/lukejenkins",
    source_type="re",
    source_detail="Reverse-engineered from 2026-04-10 Telit FN980m SDX55 LG290P-referenced 5-minute capture (23 × 811-byte records, 10 Galileo SVs tracked); legacy 30-byte format from earlier 73-sample indoor SDX55 corpus",
    source_url="https://github.com/lukejenkins",
    # 30-byte header: 3 documented binary fields (version, header_b1@1,
    # header_b2@2), all parsed. SDX55 full 811-byte format adds:
    #   - 71-byte anchor block at [30..100] — 1 identified region, unparsed
    #   - per-SV (×10): 11 binary fields parsed (sv_id_raw, status_counter,
    #     gps_tow_tick, float_a..d, misc_status_byte, flags1, float_e/f) +
    #     1 unparsed 3-byte region at slot+[12..15] (semantic TBD)
    # Legacy 30-byte form adds 5 fields (field_a, system_time, float_legacy_1..3)
    # all parsed. Net for full SDX55 path: 3 + 12 (SV: 11 parsed + 1 region)
    # + 1 (anchor block) = 16 identified, 3 + 11 = 14 parsed. Partial tier
    # — anchor block + per-SV bytes 12-15 region remain. The 13 intermediate
    # payload-size rungs (101..1166 B per #N) are unparsed at the
    # record-format level and counted separately by the inventory.
    fields_identified=16,
    fields_parsed=14,
    # Layer-2 plausibility — corpus walk 2026-05-22 against 26,974 records
    # across 15 payload sizes (30/101/172/243/314/456/527/598/669/740/811/
    # 882/953/1024/1166 B) and 10 chipset/firmware combinations:
    #   version (offset 0): 0x00 across 100% of records — corpus-wide invariant.
    # The parser currently reads `version` and emits it but does NOT validate
    # it. Declaring the enum here makes the audit toolchain surface any future
    # capture that breaks the invariant (e.g. a previously-unseen variant).
    # NOTE: header_b1 (offset 1) and header_b2 (offset 2) are chipset
    # discriminators, NOT invariants — they take values 0x04/0x06 and
    # 0x05/0x07/0x46 respectively. They cannot be declared as enum invariants
    # without a richer per-chipset spec mechanism.
    field_invariants={
        "version": {"enum": [0]},
    },
)
def parse_0x1886(log_time: int, data: bytes) -> Diag0x1886:
    """Parse a LOG_GNSS_GAL_MEASUREMENT_REPORT_C (0x1886) log payload."""
    if len(data) < 7:
        raise ValueError(f"Galileo report too short: {len(data)}")

    version = data[0]
    # Layer-1 enforcement of field_invariants version=[0] (#N). Corpus
    # walk (26,974 records / 15 payload sizes / 10 chipset-firmware combos,
    # 2026-05-22) confirms 0x00 is the only observed version byte. A foreign
    # 811B payload with byte[0] != 0 would otherwise route through the
    # full SDX55 decoder and emit plausible-but-garbage PRN / Doppler /
    # C/N0 values from unrelated wire data.
    if version != 0:
        return None
    header_b1 = data[1]
    header_b2 = data[2]

    # Full SDX55 811-byte format — but ONLY the FN980m-validated slot layout.
    # Corpus walk #N (2026-05-30) refuted the prior assumption that every
    # 811-byte / version-0 record shares the FN980m 71-byte slot layout: the
    # same 811-byte size + (header_b1, header_b2) = (6, 5) is also emitted with
    # a divergent slot layout (per-slot flags1 = 0x02c82000 instead of
    # 0x02c02006, and sv_id_raw bytes outside the Galileo 50..85 PRN range).
    # Decoding those with the FN980m offsets produced plausible-but-garbage
    # PRN / Doppler / C/N0 — the exact anti-stub pattern.  Gate the SV decode
    # on the FN980m slot signature (first-slot flags1 magic + all-slot PRN
    # validity); otherwise emit a header-only 'sdx55_811_unrecognized' record
    # so the divergence is visible instead of silently mis-decoded.
    if len(data) == _SDX55_FULL_SIZE:
        svs = [
            _parse_sv_slot(data, _SDX55_HDR_SIZE + i * _SDX55_SV_SIZE)
            for i in range(_SDX55_N_SLOTS)
        ]
        layout_ok = (
            svs[0].flags1 == _FN980M_SLOT_FLAGS1
            and all(sv.prn is not None for sv in svs)
        )
        return Diag0x1886(
            log_time=log_time,
            version=version,
            payload_size=len(data),
            header_b1=header_b1,
            header_b2=header_b2,
            header_raw=bytes(data[:_SDX55_HDR_SIZE]),
            svs=svs if layout_ok else [],
            raw_hex=data.hex(),
            parser_format='sdx55_full_811' if layout_ok else 'sdx55_811_unrecognized',
        )

    # Legacy 30-byte indoor/degraded format
    field_a = unpack_from('<H', data, 3)[0] if len(data) >= 5 else None
    system_time = unpack_from('<H', data, 5)[0] if len(data) >= 7 else None
    float_1 = unpack_from('<f', data, 12)[0] if len(data) >= 16 else None
    float_2 = unpack_from('<f', data, 17)[0] if len(data) >= 21 else None
    float_3 = unpack_from('<f', data, 21)[0] if len(data) >= 25 else None

    return Diag0x1886(
        log_time=log_time,
        version=version,
        payload_size=len(data),
        header_b1=header_b1,
        header_b2=header_b2,
        field_a_u16=field_a,
        system_time_u16=system_time,
        float_legacy_1=float_1,
        float_legacy_2=float_2,
        float_legacy_3=float_3,
        raw_hex=data.hex(),
        parser_format='legacy_30' if len(data) <= 64 else 'unknown',
    )
