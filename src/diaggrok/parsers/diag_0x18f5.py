def parse_0x18f5(log_time: int, data: bytes) -> Diag0x18F5 | None:
    n = len(data)
    if n < 1:
        return None
    if data[0] != 0x00:
        return None
    key = (n, data[0])
    variant = _18F5_VARIANTS.get(key)
    if variant is None:
        return None
    # Geometry is guaranteed by the size whitelist above, but re-derive it
    # defensively so the block array stays correct if a new size variant is
    # ever added to _18F5_VARIANTS.
    body_len = n - _18F5_HEADER_LEN
    if body_len < 0 or body_len % _18F5_BLOCK_LEN != 0:
        return None
    block_count = body_len // _18F5_BLOCK_LEN
    blocks: list[Gnss18F5Block] = []
    for i in range(block_count):
        off = _18F5_HEADER_LEN + i * _18F5_BLOCK_LEN
        blk = data[off:off + _18F5_BLOCK_LEN]
        angle = unpack_from('<f', blk, _18F5_BLOCK_ANGLE_OFF)[0]
        blocks.append(Gnss18F5Block(
            block_index=i,
            angle_rad_48=angle,
            raw=bytes(blk),
        ))
    return Diag0x18F5(
        log_time=log_time,
        version=data[0],
        variant=variant,
        raw_header=bytes(data[:_18F5_HEADER_LEN]),
        block_count=block_count,
        blocks=blocks,
        payload_size=n,
    )
