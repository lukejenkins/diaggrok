# --- Ground-truth recipe (#N) -------------------------------------------
# ⚠ SUBSYSTEM FLIP: the parser module is *titled* "LTE measurement", but the
# QXDM canonical name is LOG_GNSS_QZSS_SBAS_MEASUREMENT_REPORT and three
# independent structural signals agree it is GNSS, not LTE: (1) the geometry is
# a 30B header + N×71B per-record blocks (classic per-SV/per-signal measurement
# array), and (2) the f32 at block-rel offset 48 is a corpus-CONSTANT -pi/2 —
# the canonical Qualcomm GNSS "not-computed angle" (elevation/azimuth) sentinel.
# So this recipe grounds against GNSS references (NMEA + Sierra GPS AT), NOT LTE.
# Target: Sierra Wireless EM9291 (SDX62, SWIX65C_02.17.08.00) — emits 0x18F5 and
# has LG290P-referenced GNSS comparison captures (gnss_comparison_2026-05-2x)
# usable as ground truth for SV-count / per-SV cross-checks.

_18F5_VARIANTS: dict[tuple[int, int], str] = {
    (172, 0x00): "lte_meas_172_v00",   # 30B header + 2 blocks
    (101, 0x00): "lte_meas_101_v00",   # 30B header + 1 block (base)
    (243, 0x00): "lte_meas_243_v00",   # 30B header + 3 blocks
}

# Structural geometry resolved 2026-05-30 (#N, <redacted-ref>). Since 71 is prime
# and the corpus size-enum is {101, 172, 243}, the only header/block split
# consistent with all three sizes is a fixed 30-byte header followed by a
# variable count of 71-byte measurement blocks:
#     101 = 30 + 1*71   172 = 30 + 2*71   243 = 30 + 3*71
# This refines the earlier "101 base + appended 71B blocks" framing into a
# clean header + N-block array (same shape as 0x19BB / 0x19EB).
_18F5_HEADER_LEN = 30
_18F5_BLOCK_LEN = 71

# Block-relative offset 48 carries a little-endian f32 that decodes to
# -1.5707963 (== -pi/2) on EVERY block of EVERY fixture across three distinct
# chipsets (eg18na MDM9x55, fn980 SDX55, lm960 SDX20). A -pi/2 angle is the
# canonical Qualcomm GNSS "not-computed" sentinel for an elevation/azimuth
# field (radians). Exposed as a named per-block field; NOT yet declared as a
# layer-2 registry invariant pending a full-corpus offset sweep (#N).
_18F5_BLOCK_ANGLE_OFF = 48


@dataclass
class Gnss18F5Block:
    """Single 71-byte measurement block inside a 0x18F5 record.

    The record is ``30B header + N*71B blocks`` (N = 1/2/3 for the
    101/172/243B size variants). Only the cross-chipset f32 sentinel at
    block-relative offset 48 is named so far; the rest of the 71B is
    preserved as ``raw`` for downstream per-field RE (#N).
    """
    block_index: int
    angle_rad_48: float   # f32 @ block-rel 48 — cross-chipset -pi/2 sentinel
    raw: bytes            # full 71B block payload


@dataclass
class Diag0x18F5:
    """GNSS QZSS/SBAS measurement report (0x18F5): 30B header + N*71B block array, all v=0x00.

    CAMP: GNSS (domain="gnss") - NOT LTE. QXDM LOG_GNSS_QZSS_SBAS_MEASUREMENT_REPORT.

    QXDM canonical name is ``LOG_GNSS_QZSS_SBAS_MEASUREMENT_REPORT``; the
    fixed 30-byte header followed by a variable count of 71-byte blocks is
    consistent with a per-SV / per-signal measurement array. Body-field RE
    inside each 71-byte block is open scope (#N) beyond the -pi/2 angle
    sentinel at block-relative offset 48.
    """
    log_time: int
    version: int
    variant: str
    raw_header: bytes        # 30B header (includes version byte at offset 0)
    block_count: int         # derived: (payload_size - 30) // 71
    blocks: list[Gnss18F5Block]
    payload_size: int

    def to_dict(self) -> dict[str, Any]:
        return {
            'type': 'Diag0x18F5',
            'log_time': self.log_time,
            'version': self.version,
            'variant': self.variant,
            'block_count': self.block_count,
            'payload_size': self.payload_size,
            'blocks': [
                {
                    'block_index': b.block_index,
                    'angle_rad_48': b.angle_rad_48,
                }
                for b in self.blocks
            ],
        }


