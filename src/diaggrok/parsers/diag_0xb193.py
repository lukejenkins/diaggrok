# v48 (SDX55: RM500Q-AE / LV55 / EM9190, #N/#N/#N) — the +4 carrier-header
# drift. The SDX55 v48 carrier header is 12 B (EARFCN u32 @sp+0, num_cells u16
# @sp+4, then 4 more bytes), NOT the 8 B the legacy `>= 48` branch assumed — so
# the per-cell array starts at sp+12, shifting serving PCI from the legacy sp+8
# (decodes the 0xffff0100 carrier tail -> low9 256) to sp+12 (true PCI). Validated
# 2026-06-19 (<redacted-ref>, RM500Q-AE WLSN, SIM-less LTE LIMSRV AT&T): decoded PCI
# {473,37,277} == AT+QENG="servingcell" <pcid> across reselections + co-captured
# verified 0xB116 config_word. Expressed as pci_off=4 over the 8 B base (mirrors
