_V48_PCI_OFF = 4

# v18 (MDM9207: EG25-G / EC25 family, #N) — older small-regime carrier-header
# layout. Unlike v36/v48/v59 (which carry a num_cells count at sp+4 followed by
# per-cell records), v18 packs the serving cell INLINE in the carrier header and
# has NO num_cells field at sp+4 — reading sp+4 as a u16 count yields garbage
# (e.g. 0x1136 = 4406). The serving cell is: EARFCN @ sp+0 (u32, low 18 bits),
# serving PCI @ sp+4 (u32, low 9 bits). Reverse-engineered + validated 2026-06-10
# on EG25-G @ A0.301.A0.301 (SIM-less LTE LIMSRV, <redacted-ref>): EARFCN 2050 +
# PCI 310 == AT+QENG="servingcell" AND the co-captured already-verified 0xB116
# serving-PCI field (310) — cross-DIAG + AT agreement. The per-cell RSRP/RSRQ
# byte layout is NOT yet RE'd for v18, so v18 entries carry pci+earfcn with
# rsrp/rsrq=None. Only the serving cell is observed SIM-less (no neighbours
# camped); larger v18 payloads (multi-cell) are not yet characterised.
#
# RSRP-field search (2026-06-27, #N/#N): scanned the 100B v18 payload at
# every i8/i16/u16/i32 offset against the co-captured AT ground truth on
# 20260622T2135Z-EG25-G (AT QENG servingcell RSRP -100..-104, RSSI -67..-71,
# RSRQ -11..-13 over the poll). RESULT — the v18 RSRP field is NOT locatable
# from this capture: the only fields whose value sits near the AT RSRP are
# CONSTANT across all 245 records (payload off=7 i16 == -1024 == -102.4 dBm at a
# /10 scale; off=21 i8 == -105) and therefore do NOT track the AT's ~4 dB RSRP
